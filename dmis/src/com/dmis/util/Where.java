package com.dmis.util;

/**
 * where工具类
 * @author Administrator
 *
 */
public class Where {

	private String parm;
	
	private String op;
	
	private String value;

	public String getParm() {
		return parm;
	}

	public void setParm(String parm) {
		this.parm = parm;
	}

	public String getOp() {
		return op;
	}

	public void setOp(String op) {
		this.op = op;
	}

	public String getValue() {
		return value;
	}

	public void setValue(String value) {
		this.value = value;
	}

}
